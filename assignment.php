<?php
    $x = 10;

    #1.Add and Assignment value
    echo "$x += 20 =",$x += 20, "<br>"; //x = x + 20 //

    #2.Subtract Assignment value
    echo "$x -= 20 =",$x -= 20, "<br>"; //x = x - 20 //

    #3.Multiply Assignment value
    echo "$x *= 20 =",$x *= 20, "<br>"; //x = x * 20 //

    #4.Divisio Assignment value
    echo "$x /= 20 =",$x /= 20, "<br>"; //x = x / 20 //

    #5.Modulus Assignment value
    echo "$x %= 20 =",$x %= 20, "<br>"; //x = x % 20 //

    #6.Exponential Assignment value
    echo "$x **= 20 =",$x **= 20, "<br>"; //x = x ** 20 //
?>